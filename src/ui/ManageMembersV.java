    private void btnCheckDupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckDupActionPerformed

        if(txtMemberId.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Please input Member ID" );
        }else{
           // TODO add your handling code here:
           
            try {
                boolean checkMemDup = memberController.checkMemDup(txtMemberId.getText().trim());

                if(checkMemDup){
                    JOptionPane.showMessageDialog(this, "Member Id already exist" );

                    chkBtnDup= false;
                }else{
                    JOptionPane.showMessageDialog(this, "Member Id can use" );
                    txtMemberId.setEnabled(false);
                    btnCheckDup.setEnabled(false);
                    chkBtnDup = true;
                }

            } catch (SQLException ex) {
                ex.printStackTrace();
            } 
        }
        
        
        
    }//GEN-LAST:event_btnCheckDupActionPerformed

