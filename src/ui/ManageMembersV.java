    public void showData(){
        
        DefaultTableModel tableModel = (DefaultTableModel) jTableMember.getModel();
        tableModel.setRowCount(0);
        
        ArrayList<LibraryMember> list = (ArrayList<LibraryMember>) memberController.getAllLibraryMember();
        
        Object rowData[] = new Object[4];
        for(int i=0; i<list.size(); i++){
            
            rowData[0] = list.get(i).getMemberId();
            rowData[1] = list.get(i).getFirstName();
            rowData[2] = list.get(i).getLastName();
            rowData[3] = list.get(i).getPhone();
            tableModel.addRow(rowData);
        }
        
        
    }

